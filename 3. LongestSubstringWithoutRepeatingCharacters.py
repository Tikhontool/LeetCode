# Given a string s, find the length of the longest
# substring without repeating characters.


# Нейронка
def length_of_longest_substring(s):
    n = len(s)
    ans = 0
    # Создаем словарь для отслеживания последнего вхождения символа
    char_map = {}
    i = 0
    # Используем два указателя, чтобы отслеживать текущую подстроку без повторов
    for j in range(n):
        if s[j] in char_map:
            # Если символ уже встречался, перемещаем левый указатель на позицию следующую за последним вхождением этого символа
            i = max(char_map[s[j]], i)
        # Обновляем максимальную длину подстроки
        ans = max(ans, j - i + 1)
        # Обновляем последнее вхождение символа
        char_map[s[j]] = j + 1
    return ans


# Ошибка с преувеличением размера стека вызовов функции
class Solution(object):
    def lengthOfLongestSubstring(self, s):
        """
        :type s: str
        :rtype: int
        """

        return self.test(s, str(), 0, [])

    def test(self, original_s, unique_char, index, list_string):
        if len(original_s) >= 1 and index < len(original_s):
            if original_s[index] not in unique_char:
                unique_char = unique_char + original_s[index]
                index += 1
                list_string.append(unique_char)
                self.test(original_s, unique_char, index, list_string)
            else:
                self.test(original_s[1:], str(), 0, list_string)
        return len(max(list_string, key=lambda x: len(x))) if len(list_string) else 0


